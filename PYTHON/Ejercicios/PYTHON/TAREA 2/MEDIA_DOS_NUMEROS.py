# Este programa medira la media aritmetica entre dos numeros
print("CALCULO DE LA MEDIA DE DOS NUMEROS")
N1= input ("Escriba un numero ")
N2= input("Escriba otro número ")
A=eval(N1)
B=eval(N2)
